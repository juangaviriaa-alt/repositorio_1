Create database Empleadores
Use Empleadores
Create table Empleadores(
ID_E-Int Primary key, // en el Int se guarda el ID del empleador, el cual se le asignara un numero unico a cada empleador.
Nombre_E-Varchar, // en el Varchar se guarda el nombre del empleador.
Apellidos_E-Varchar, // en el Varchar se guardaran los apellidos del empleador.
Forma_Pago-Int); // en el Int se guarda la forma de pago que puede ser semanal, quincenal o mensual.

